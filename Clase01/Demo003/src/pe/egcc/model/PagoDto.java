
package pe.egcc.model;

public class PagoDto 
{
    private int horasxdia;
    private int dias;
    private double pagoxhora;
    private double Ingresos;
    private double Renta;
    private double Neto;

    /**
     * @return the horasxdia
     */
    public int getHorasxdia() {
        return horasxdia;
    }

    /**
     * @param horasxdia the horasxdia to set
     */
    public void setHorasxdia(int horasxdia) {
        this.horasxdia = horasxdia;
    }

    /**
     * @return the dias
     */
    public int getDias() {
        return dias;
    }

    /**
     * @param dias the dias to set
     */
    public void setDias(int dias) {
        this.dias = dias;
    }

    /**
     * @return the pagoxhora
     */
    public double getPagoxhora() {
        return pagoxhora;
    }

    /**
     * @param pagoxhora the pagoxhora to set
     */
    public void setPagoxhora(double pagoxhora) {
        this.pagoxhora = pagoxhora;
    }

    /**
     * @return the Ingresos
     */
    public double getIngresos() {
        return Ingresos;
    }

    /**
     * @param Ingresos the Ingresos to set
     */
    public void setIngresos(double Ingresos) {
        this.Ingresos = Ingresos;
    }

    /**
     * @return the Renta
     */
    public double getRenta() {
        return Renta;
    }

    /**
     * @param Renta the Renta to set
     */
    public void setRenta(double Renta) {
        this.Renta = Renta;
    }

    /**
     * @return the Neto
     */
    public double getNeto() {
        return Neto;
    }

    /**
     * @param Neto the Neto to set
     */
    public void setNeto(double Neto) {
        this.Neto = Neto;
    }
    
    
    
}
